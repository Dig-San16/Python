#Os operadores númericos vão existir em quase todas as linguagens de programação,
#em Python, existe 7 deles:

# "+" = adição

# "-" = subtração

# "*" = multiplicação

# "/" = divisão com resultados DECIMAIS

# "//" = divisão com resultados INTEIROS

# "%" = módulo ou resto da divisão

# "**" = potenciação ou exponenciação

print(2 + 2)
print(2 - 2)
print(2 * 3)
print(11 / 2)
print(11 // 2)
print(6 % 2)
print(6 ** 2)


