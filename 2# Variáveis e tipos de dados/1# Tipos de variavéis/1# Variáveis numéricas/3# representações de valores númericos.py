#A representação de valores numéricos são definidos quando
#Os computadores usam sistemas de numeração binária (base 2) para representar
#números, geralmente representados por 0 e 1.

print(2 % 2) #<- resulta em 0 (par)

print(3 % 2) #<- resulta em 1 (impar)

#existem vários tipos comuns de representação númerica, são eles:


#DECIMAL(Base 10):

#O sistema numérico usado pelos seres humanos, baseado em 10
#dígitos (0 a 9). Computadores muitas vezes convertem números decimais para
#binários para processamento interno.

#BINÀRIO (Base 2):

#O sistema fundamental para representação de números em
#computadores. Os dígitos são 0 e 1. Cada dígito em um número binário é chamado
#de "bit" (abreviação de binary digit).

#OCTAL (Base 8):

#Menos comum, mas ainda usado, especialmente em programação.
#Cada dígito octal representa três bits.

#HEXADECIMAL (Base 16):

#Amplamente utilizado em programação de baixo nível e
#representação de cores. Cada dígito hexadecimal representa quatro bits.



