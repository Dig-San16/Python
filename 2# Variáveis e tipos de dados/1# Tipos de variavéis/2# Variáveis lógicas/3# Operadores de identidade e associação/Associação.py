#Os operadores de associação são
#utilizados para verificar a associação de um valor
#a uma sequência de dados, como uma lista, tupla, string ou conjunto.

#Nas operações de associação, são usados as seguintes Palavras:

lista = [1, 2, 3, 4, 5]


#'IN': Verifica se um valor está presente em uma sequência.

print(3 in lista)  
print(6 in lista)

#'NOT IN': Verifica se um valor não está presente em uma sequência.

print(3 not in lista) 
print(6 not in lista)  
