#Os operadores de atribuições especiais são responsaveis por simplificar
#as variáveis de repetição (contadores e acumuladores), em Python, existe 6
#deles:

# " += " = adição

# " -= " = subtração

# " *= " = multiplicação

# " /= " = divisão (inteiro)

# " //= " = divisão (decimal)

# " **= " = potenciação ou exponenciação

x = 0
while x < 10:
    x += 1
    print(x)
    


    
 
