#FOR é utilizado para repetir um bloco, uma forma bem parecida com o WHILE,
#porém, FOR é utilizado para iterar sobre uma sequência(listas, conjuntos...)
#e executar um bloco de código para cada elemento na sequência.

l = [34,45,4] 
for num in l: #1
    print(num)

#Este é um loop for que itera sobre os elementos da lista l. IN é um operador
#de ASSOCIAÇÂO responsavel por verificar se todos os valores estão
#presentes na sequência

#vale lembrar que FOR não substitui o WHILE, ambas são estruturas de repetição
#mas diferem na forma como controlam o fluxo.

#utilizamos FOR quando sabemos antecipadamente quantas vezes repetir um código

#o WHILE É utilizado quando os números de iterações não é
#conhecido antecipadamente
