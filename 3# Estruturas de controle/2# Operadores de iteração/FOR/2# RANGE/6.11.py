#RANGE é basicamente um gerador que repete valores de uma sequencia em um
#intervalo especifico. Para iterar sobre uma sequência de números. A
#sintaxe básica da função range é a seguinte:

#   RANGE(início, fim, passo)

# início: O valor inicial da sequência (por padrão, começa em 0).
# fim: O valor final da sequência (o intervalo não inclui esse valor).
# passo: O intervalo entre os números (por padrão, é 1).


#A função range retorna um objeto de sequência que pode ser convertido
#em uma lista ou usado em loops diretamente, veja:

for i in range(4): 
    print(i)

