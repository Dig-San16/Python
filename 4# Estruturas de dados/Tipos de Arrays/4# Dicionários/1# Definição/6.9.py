#Diciónarios são arrays associativos, basicamente, armazenam sequencias de "chave-valor"

Brasil = {"capital": "Brasília", "idioma": "Portugues", "população": 210}
print(type(Brasil)) #<-- Variável do tipo DICT

#Diferentemente dos seus antecessores, os diciónarios possuem como "indices"
#as chaves(capital, idioma, população), essas chaves ASSOCIAM os valores
#(Brasília, Portugues, 210), funcionando como uma subvariável.

print(len(Brasil))

