#A função MAP em Python é utilizada para aplicar uma função a cada elemento de
#um iterável (por exemplo, uma lista) e retornar um novo iterável contendo os
#resultados. 

variável = map(função, coleção)

#A 'função' será aplicada a cada elemento do coleção.

